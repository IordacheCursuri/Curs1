package com.itFactory;

public class HelloWorld {

    // public -> un modificator de acces. Face ca metoda sa fie vizibila in tot proiectul (comentariu pe o singura linie)

/*
* static -> nu avem nevoie de instanta/obiect pentru a apela aceasta metoda
* void -> inseamna ca aceasta metoda nu returneaza nimic
*  ---> acesta este un comentariu pe mai multe linii
*
* */


/**
 * main -> aceasta este metoda principala care porneste programul
 * Fara aceasta metoda nu putem porni programul
 * String[] args -> un array de tip string si preprezinta argumentele programului
 *
 * */

    public static void main (String[] args) {
        System.out.println("Hello World");
    }

}